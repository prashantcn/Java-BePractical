package com.fundamentals;

public class Datatypes_I {

	public static void main(String[] args) {
		System.out.println("Size of Diff data types");
		System.out.println("Byte Size "+Byte.SIZE+" bits");
		System.out.println("Short Size: "+Short.SIZE+" bits");
		System.out.println("Int Size: "+Integer.SIZE+" bits");
		System.out.println("Long Size: "+Long.SIZE+" bits");
		System.out.println("Float Size: "+Float.SIZE+" bits");
		System.out.println("Double Size: "+Double.SIZE+" bits");
		System.out.println("Char Size: "+Character.SIZE+" bits");
		
		System.out.println("Min Value");
		System.out.println("Byte Size "+Byte.MIN_VALUE+" bits");
		System.out.println("Short Size: "+Short.MIN_VALUE+" bits");
		System.out.println("Int Size: "+Integer.MIN_VALUE+" bits");
		System.out.println("Long Size: "+Long.MIN_VALUE+" bits");
		System.out.println("Float Size: "+Float.MIN_VALUE+" bits");
		System.out.println("Double Size: "+Double.MIN_VALUE+" bits");
		System.out.println("Char Size: "+Character.MIN_VALUE+" bits");

	}

}
